package com.paevolution.appiogateway.exceptions.model;

import java.io.Serializable;
import java.util.Date;
import java.util.List;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonPropertyOrder;

import io.swagger.v3.oas.annotations.media.Schema;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@AllArgsConstructor
@NoArgsConstructor
@JsonPropertyOrder(value = { "timestamp", "title", "status", "details" })
public class ErrorResponse implements Serializable {

    /**
     * 
     */
    private static final long serialVersionUID = -1956045892625013942L;
    @JsonProperty("timestamp")
    private Date date;
    @Schema(description = "A short, summary of the problem type. Written in english and readable\r\n"
	    + "for engineers (usually not suited for non technical stakeholders and\r\n" + "not localized); example: Service Unavailable")
    private String title;
    @Schema(description = "The HTTP status code generated by the origin server for this occurrence of the problem.", minimum = "100", maximum = "600", exclusiveMaximum = true, example = "200")
    private Integer status;
    @Schema(description = "A human readable explanation specific to this occurrence of the problem.", example = "Validation error while processing a request.", minLength = 0)
    private List<String> details;
}
